"use client";
import Link from "next/link";
import { usePathname } from "next/navigation";
import { Button } from "@/components/ui/button";
import {
  CardTitle,
  CardDescription,
  CardHeader,
  CardContent,
  Card,
} from "@/components/ui/card";

function Nav() {
  const pathname = usePathname();

  const navItems = [
    { href: "/", icon: LayoutDashboardIcon, label: "Dashboard" },
    { href: "/student", icon: UsersIcon, label: "Students" },
    { href: "/pickup", icon: CarIcon, label: "Pick-up" },
    { href: "/notifications", icon: MegaphoneIcon, label: "Announcements" },
  ];

  return (
    <div className="sticky top-0 h-screen w-64 flex-shrink-0">
      <div className="h-full border-r bg-gray-100/40 dark:bg-gray-800/40">
        <div className="flex h-full flex-col">
          {/* Header */}
          <div className="flex h-[60px] items-center border-b px-6">
            <Link className="flex items-center gap-2 font-semibold" href="#">
              <SchoolIcon className="h-6 w-6" />
              <span>School Admin</span>
            </Link>
            <Button className="ml-auto h-8 w-8" size="icon" variant="outline">
              <BellIcon className="h-4 w-4" />
              <span className="sr-only">Toggle notifications</span>
            </Button>
          </div>
          
          {/* Nav Items */}
          <div className="flex-1 overflow-y-auto">
            <div className="py-2">
              <ul className="grid items-start px-4 text-sm font-medium">
                {navItems.map((item) => {
                  const isActive = pathname === item.href;
                  const Icon = item.icon;
                  return (
                    <li key={item.href}>
                      <Link
                        className={`flex items-center gap-3 rounded-lg px-3 py-2 transition-all hover:text-gray-900 dark:hover:text-gray-50 ${
                          isActive
                            ? "bg-gray-100 text-gray-900 dark:bg-gray-800 dark:text-gray-50"
                            : "text-gray-500 dark:text-gray-400"
                        }`}
                        href={item.href}
                      >
                        <Icon className="h-4 w-4" />
                        {item.label}
                      </Link>
                    </li>
                  );
                })}
              </ul>
            </div>
            
            {/* Help Card */}
            <div className="p-4">
              <Card>
                <CardHeader className="pb-4">
                  <CardTitle>Need Help?</CardTitle>
                  <CardDescription>
                    Contact our support team for any questions or issues.
                  </CardDescription>
                </CardHeader>
                <CardContent>
                  <Button className="w-full" size="sm">
                    Get Support
                  </Button>
                </CardContent>
              </Card>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
}

export default Nav;

